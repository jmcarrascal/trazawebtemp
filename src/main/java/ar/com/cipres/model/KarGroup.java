package ar.com.cipres.model;

public class KarGroup {
	
	private Long contador;
	private Integer transacorigen;
	private String clave;
	
	public Long getContador() {
		return contador;
	}
	public void setContador(Long contador) {
		this.contador = contador;
	}
	public Integer getTransacorigen() {
		return transacorigen;
	}
	public void setTransacorigen(Integer transacorigen) {
		this.transacorigen = transacorigen;
	}
	public String getClave() {
		return clave;
	}
	public void setClave(String clave) {
		this.clave = clave;
	}
	
	
	

}
